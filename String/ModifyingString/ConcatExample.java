package InfinityJune21.String.ModifyingString;

public class ConcatExample {
    public static void main(String[] args) {
        String s1 = "brown";
        String s2 = "fox";

        String s3 = s1.concat(s2);

        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
